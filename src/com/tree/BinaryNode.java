package com.tree;

public class BinaryNode {

	public int value;
	public BinaryNode leftPointer;
	public BinaryNode rightPointer;
	
	public BinaryNode(int value, BinaryNode leftPointer, BinaryNode rightPointer) {
	
		this.value = value;
		this.leftPointer = leftPointer;
		this.rightPointer = rightPointer;
	}
	
	
}
